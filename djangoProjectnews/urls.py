from home.views import index
from news.views import NewsListView, NewsCreateView, NewsDetailView, NewsUpdateView, NewsDeleteView

from django.contrib import admin
from django.urls import path
from django.conf import settings
from django.conf.urls.static import static

urlpatterns = [
    path('admin/', admin.site.urls),
    path('news/', NewsListView.as_view()),
    path('news/create/', NewsCreateView.as_view()),
    path('news/<int:pk>/', NewsDetailView.as_view()),
    path('news/<int:pk>/update/', NewsUpdateView.as_view()),
    path('news/<int:pk>/delete/', NewsDeleteView.as_view()),
    path('', index)
]

urlpatterns += static(settings.STATIC_URL, document_root=settings.STATIC_ROOT)
urlpatterns += static(settings.MEDIA_URL, document_root=settings.MEDIA_ROOT)
